package com.example.javadersleri.degiskenler;

public class Sabitler {
    public static void main(String[] args) {
        // Sabit - Constant - final
        int sayi = 10;
        System.out.println(sayi);
        sayi = 80;
        System.out.println(sayi);

        final int numara = 100;
        System.out.println(numara);
        // numara = 300;

    }
}
